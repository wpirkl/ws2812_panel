static uint8_t y8 = 1;

/* possible shifts to achieve full-cycle

a8, b8, c8:
(1, 1, 2)   (1, 1, 3)   (1, 7, 3)   (1, 7, 6)   (1, 7, 7)   (2, 1, 1)
(2, 5, 5)   (3, 1, 1)   (3, 1, 5)   (3, 5, 4)   (3, 5, 5)   (3, 5, 7)
(3, 7, 1)   (4, 5, 3)   (5, 1, 3)   (5, 3, 6)   (5, 3, 7)   (5, 5, 2)
(5, 5, 3)   (6, 3, 5)   (6, 7, 1)   (7, 3, 5)   (7, 5, 3)   (7, 7, 1)

*/
uint8_t xorshift8(void) {

    y8 ^= (y8 << 7);
    y8 ^= (y8 >> 5);
    y8 ^= (y8 << 3);

    return y8;
}


